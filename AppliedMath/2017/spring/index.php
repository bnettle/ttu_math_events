 <em>
  Fracture Model Reduction and Optimization for Nonlinear Flows in Porous Media
 </em> <br>
   Pushpi Paranamana
 <br>
  <a id="toggle_abst_march29"> abstract </a> 
  </td>
</tr>
 </table> 


  <span class="abst" id="abst_march29"> 
In this work, we analyze the flow filtration process of slightly compressible fluids in porous media containing fractures with complex geometries. We model the coupled fracture-porous media system where the linear Darcy flow is considered in porous media and the nonlinear Forchheimer equation is used inside the fracture. The optimal length of the fracture is analyzed using “the diffusive capacity”, a functional that measures the performance of the reservoir. 
Also, we devise a model to address the complexity of the fracture geometry which examines the flow inside fractures with variable thickness on a general manifold. The fracture is represented as a parametric surface on Riemannian manifold where the thickness changes in the normal direction from the barycentric surface. Using Laplace Beltrami operator, we formulate an equation that describes the flow and then further simplifications were done. Using the model, pressure profile of a nonlinear flow is analyzed and compared with the actual pressure profile obtained numerically in order to validate the model.
  </span>

 
