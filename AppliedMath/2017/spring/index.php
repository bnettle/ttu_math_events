 <em>
   The interaction between fluids and solids: modeling, discretization, simulations (Joint Physics-Math Colloquium)
 </em> <br>
     Giorgio Bornia
 <br>
 <a id="toggle_abst_april13"> abstract </a>
  </td>
</tr>
 </table> 
 
 
  <span class="abst" id="abst_april13"> 
   Multiphysics problems attract an increasing attention in the mathematical community.
 In fact, their study leads to a deeper understanding of real-life processes.
 Among them, the interaction of fluids and solids is certainly 
 one of the most interesting, with ubiquitous applications in fields such as 
 aeroelasticity, biomechanics and civil engineering.
 We will explore the two-way coupling between fluid and solid mechanics
 in a broad perspective. The physical modeling,
 the definition of numerical algorithms
 and their computer implementation will be discussed, 
 in order to highlight the main challenges
 as well as the effectiveness in the simulation of real-world phenomena.
  </span>

 
